// 사전에서 단어 찾기
export function findWordsStartingWith(char: string): string[] {
  const variants = getDueumVariants(char);
  const results: string[] = [];
  for (const v of variants) {
    if (WORD_DICT[v]) results.push(...WORD_DICT[v]);
  }
  return results;
}

// 사전에 단어가 있는지 확인
export function isInDict(word: string): boolean {
  const firstChar = word[0];
  const words = WORD_DICT[firstChar];
  if (words && words.includes(word)) return true;
  // 모든 사전 검색
  for (const list of Object.values(WORD_DICT)) {
    if (list.includes(word)) return true;
  }
  return false;
}
