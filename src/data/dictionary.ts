// 두음법칙 변환 맵
export const DUEUM_MAP: Record<string, string> = {
  '녀': '여', '뇨': '요', '뉴': '유', '니': '이',
  '랴': '야', '려': '여', '례': '예', '료': '요',
  '류': '유', '리': '이', '라': '나', '래': '내',
  '로': '노', '뢰': '뇌', '루': '누', '르': '느',
};

// 두음법칙 적용
export function applyDueum(char: string): string {
  return DUEUM_MAP[char] || char;
}

// 두음법칙 변형 포함 모든 가능한 첫 글자 반환
export function getDueumVariants(char: string): string[] {
  const variants = [char];
  const applied = applyDueum(char);
  if (applied !== char) variants.push(applied);
  // 역방향도 확인
  for (const [from, to] of Object.entries(DUEUM_MAP)) {
    if (to === char && !variants.includes(from)) variants.push(from);
  }
  return variants;
}

// 마지막 글자 추출
export function getLastChar(word: string): string {
  return word[word.length - 1];
}

